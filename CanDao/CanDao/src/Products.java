
public class Products {
	
	private int  pid;
	 private int  num;
	 private String  name;
	 private float price;
	 
	    public Products() {
	    }
	 

	    public int getPid() {
	        return pid;
	    }
	 
	    public void setPid(int pid) {
	        this.pid = pid;
	    }
	    
	    
	 
	    public String getName() {
	        return name;
	    }
	 
	    public void setName(String name) {
	        this.name = name;
	    }
	 
	    public int getNum() {
	        return num;
	    }
	 
	    public void setNum(int num) {
	        this.num = num;
	    }
	    
	    public float getPrice() {
	        return this.price;
	    }
	 
	    public void setPrice(Float price) {
	        this.price = price;
	    }

}
